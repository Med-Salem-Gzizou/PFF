echo config hostapd ...
#Edit the hostapd configuration file and create a wireless network "/etc/hostapd/hostapd.conf"
cat >/etc/hostapd/hostapd.conf <<EOL
interface=wlan0
driver=nl80211
#driver=rtl871xdrv
ssid=PiHome
hw_mode=g
channel=6
macaddr_acl=0
auth_algs=1
ignore_broadcast_ssid=0
wpa=2
wpa_passphrase=raspberry
wpa_key_mgmt=WPA-PSK
wpa_pairwise=TKIP
rsn_pairwise=CCMP
EOL

